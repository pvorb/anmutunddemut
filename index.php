		<h2 class="title"><a href="/2010/08/03/reductio-ad-naturum.html">Reductio ad Naturam</a></h2>
		<p class="metadata">
			<span class="date">3. 8. 2010<br/></span>			
		</p>
	
    <div class="content">
      <p class='teaserimage'><img src='http://anmutunddemut.de/files/reductionadnaturam.jpg' alt='Reductio ad Naturam'/></p>    
      <p>Ein weiteres <a href="http://anmutunddemut.de/lexikon/scheinargument.html">Scheinargument</a>, dass ich nicht mehr gelten lasse, bezeichne ich als Reduction ad Naturam. Es bezeichnet jene Argumente, die einen wie auch immer gearteten vorhistorische Naturzustand der menschlichen Entwicklung heranziehen, um irgendwelche bewußten Verhaltensweisen lebender Menschen zu erklären. So zum Beispiel die Idee, dass das stete Reagieren auf Blinken und Piepsen von Gagdets sich davon ableiten ließe, dass man ja auf wilde Tiere, die im Gebüsch lauerten, als Urmensch auch schnell reagieren müsse.</p>
