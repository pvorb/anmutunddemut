      <h2>Die Wege des gesunden Menschenverstands</h2>

<!-- start:teaserimage -->
<p class='teaserimage'><img src='http://anmutunddemut.de/files/gesundermenschenverstand.png' alt='Die Wege des gesunden Menschenverstands'/></p>
<!-- ende:teaserimage -->
<p>Ihr kennt das sicher auch. Jemand versucht einem etwas beizubringen. Leider widerspricht es der eigenen Intuition und dem scheinbar gesunden Menschenverstand. Ich habe mich in meiner Vergangenheit regelmäßig sehr energisch gewehrt in solchen Situationen. Ich konnte nicht glauben, dass Decken nicht <em>wärmen</em>, sondern isolieren: Mir wollte nicht einleuchten, dass ein Eiswürfel den man in deine Daumendecke hüllt nicht nur nicht schneller schmilzt sondern sogar langsamer schmilzt.  Ich wollte nicht wahr haben, dass zwei gleich Kugeln gleichschnell zur Erde fallen, gleich wie schnell man eine von beiden auch noch horizontal beschleunigt. Ich habe fast einen halben Tag mit meinem Mitbewohner über <a href="http://de.wikipedia.org/wiki/Ziegenproblem">das Ziegenproblem</a> gestritten. Hinterher hab ich mich dann immer verhältnismäßig kleinlaut natürlich doch überzeugen lassen.</p>
<p>Ein Einsicht für die ich viel viel länger gebraucht habe ist diese: Ein Philosophie-Professor von mir hat mal die These geäußert, dass die Menschen nicht ernsthaft wollen, dass ihre Wünsche in Erfüllung gehen. Das scheint wahlweise völlig misanthropisch oder völlig irre zu sein. Wenn ich aber eines in diesem Jahrzehnt, im Studium und den ersten Berufsjahren, eingesehen und gelernt habe, dann genau das. Die Erfüllung von Wünschen führt nicht zum Glück. Im Gegenteil. Erfüllte Wünsche führen nur zu anderen Wünschen. <em>Dort</em> ist kein Glück zu finden. Glück ist nur <em>hier</em>, immer. Wenn ich <em>hier</em> nicht glücklich bin, werde ich es dort auch nicht sein.</p>
        
