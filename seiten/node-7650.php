      <h2>Star Wars - The Clone Wars</h2>

<!-- start:teaserimage -->
<p class='teaserimage'><img src='http://anmutunddemut.de/files/starwars-clonewars.jpg' alt='Star Wars - The Clone Wars'/></p>
<!-- ende:teaserimage -->
<p>Gestern gekauft. Heute schon geschaut. Rockenroll! Star Wars gibt Gummi. Endlich. Auch wenn ich die exzentrische und experimentierfreude Zeichentrickserie "<a href="http://anmutunddemut.de/2008/08/23/star-wars-clone-wars-die-zeichtentrickserie">Star Wars – Clone Wars</a>" (nicht "The"!) gerne mochte, der Film ist doch nochmal hicerer Shice. Zum einen wird mehr geballert. Zum anderen ballern v.a. mehr Klonkrieger und AT-TE Walker. Ich mag die Klonkrieger weil sie alle von <a href="http://www.synchronkartei.de/index.php?action=show&amp;type=talker&amp;id=328">Martin Keßler</a> – dem Synchronsprecher von Nicolas Cage und Riddick – gesprochen werden. Und die AT-TE mag ich fast noch mehr, weil sie aussehen wie Schildkröten. </p>
<p>Außerdem finde ich es überhaupt großartig dass mal ein vollständiger Film während der Klonkriege spielt. Episode 2 ist ja der Anfang und Episode 3 das Ende der Klonkriege. Herrlich. Krieg. Und wie gut sieht General Kenobi mal bitte aus?! Das ist der Kenobi, den ich immer sehen wollte. Das ist Star Wars, wie ich es immer sehen wollte. Die Animationsform gefällt mir dabei sogar eigentlich besser als die Filme. Jetzt müssen nicht mehr Menschen gegen Effekte anspielen. Alles ist Effekt, ist Fiktion aus einem Guß.</p>
<p>Yeah! Ick freu mir schon'n Kecks in'nen Arsch auf die Clone Wars Serie. Mehr und mehr und mehr davon. Und, ach ja, Happy <a href="http://en.wikipedia.org/wiki/Star_Wars_Day">Star Wars Day</a>! May the 4th be with you!</p>
<p><img src="http://anmutunddemut.de/files/at-te.jpg" alt="AT-TE" /></p>
        
