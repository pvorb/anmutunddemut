      <h2>Sort by Magic</h2>

<!-- start:teaserimage -->
<p class='teaserimage'><img src='http://anmutunddemut.de/files/sortbymagic.jpg' alt='Sort by Magic'/></p>
<!-- ende:teaserimage -->
<p>Die Liebe war nie groß. Aber inzwischen nutze ich twitter nur noch um meine Langeweile in die Welt zu flüstern. Mein relevante Nachrichten Quelle ist wieder Google Reader geworden. Und was Google hier Schritt für Schritt baut ist einfach viel geiler als Twitter. Neuestes Feautre: "<a href="http://googlereader.blogspot.com/2009/10/reading-gets-personal-with-popular.html">Sort by Magic</a>." Google wird siegen, aus dem einfachen Grund, weil sie es besser können und es besser machen.</p>
<p>Und scheinbar unglaublich langsam zieht der A380 weiter seine Kreise über der Stadt.</p>
        
