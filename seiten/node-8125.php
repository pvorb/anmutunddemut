      <h2>Drupalaska – North to the Future!</h2>

<!-- start:teaserimage -->
<p class='teaserimage'><img src='http://anmutunddemut.de/files/drupalaska.jpg' alt='Drupalaska – North to the Future!'/></p>
<!-- ende:teaserimage -->
<p>Mit den <a href="http://anmutunddemut.de/2009/09/29/pimp-my">Update auf ein neues Hostingg-Paket</a> vor 14 Tagen, kam auch ein Shell-Zugang daher. Mit großer Freude habe ich eben bemerkt, dass auf meinem Server auch schon Git läuft. Also flux einen Key generiert, ein neues Repository in Github erstellt und schon geht's los: <a href="http://github.com/benthebear/Drupalaska">Drupalaska</a> – North to the Future!</p>
<p>Nebenbei … das <a href="http://anmutunddemut.de/2009/10/17/drupal-theme-fucking">Thememe-Modul</a> macht schon <a href="http://anmutunddemut.de/2009/10/17/drupal-theme-fucking">gute Fortschritte</a>. In mir brennt racheesque Begeisterung, weil ich eine Reverse-Engeneering-funktion gebaut habe, die mir aus fertig-gethemeten HTML wieder strukturierte Arrays baut. Wuhaha! Wer sich das neue Theme schon ansehen mag und hier registrierter User ist, kann sich einloggen und in seinem Profil "Takeshi" als Theme einrichten.</p>
        
