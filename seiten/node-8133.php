      <h2>Eben weil</h2>

<!-- start:teaserimage -->
<p class='teaserimage'><img src='http://anmutunddemut.de/files/ebenweil.jpg' alt='Eben weil'/></p>
<!-- ende:teaserimage -->
<p>Ich hab hier die letzten Tage nicht viel geschrieben, weil ich erst mein neues Theme fertich machen wollte. Das ist ja auch Bloggen irgendwie. Ich muss aber doch nochmal was loswerden. Leute, das wird kein gutes Ende nehmen … dieses ganze Internet-Dings. Und damit meine ich nicht nur die Pläne der neuen <a href="http://chefarztfrau.de/?p=958#comment-41177">Hornissen-Koalition</a>, welche bald unsere neue Regierung hervorbringen wird. Und ich meine auch nicht die inzwischen zur Selbstverständlichkeit gewordene Abwesenheit von Datenschutz. Nein, es steht einfach zuviel auf dem Spiel – für die Arschlöcher.</p>
<p>Ein ganze Haufen von Politikern, Businesskasparn und Moralaposteln fängt gerade erst an zu erkennen, was hier geht, was hier machbar ist. Und glaubt mir, wenn sie nicht eh schon am längeren Hebel sitzen, dann werden sie uns einfach korrumpieren. Wir werden das Schicksal unserer Eltern teilen. Eben <em>weil</em> wir recht hatten. Eben <em>weil</em> wir mit unserem Internet Erfolg hatten, <em>weil</em> wir alles umgewälzt haben, <em>weil</em> das Netz sich in alle Lebensbereiche ausstreckt. Wir sind die neuen 68er. Heute Love, Peace und Filesharing. Die letzten 10 Jahre im Netz waren unser Woodstock. Und morgen kommen die Yuppies und die Lohas. Morgen der Genosse der Bosse und im Aufsichtsrat der Telekom.</p>
<p>Let's face it: Von hier an wird's nur noch schlimmer. Dragons beyond this point. Lest <a href="http://de.wikipedia.org/wiki/Fahrenheit_451">Farenheit 451</a>! Als Buch! Alles andere wird euch schon bald als <a href="http://www.daten-speicherung.de/index.php/bundesinnenministerium-plant-hochgeruesteten-sicherheitstaat/">Werbung für eine terroristischen Vereinigung</a> ausgelegt werden.</p>
        
