      <h2>Das Mädchen aus dem Wasser</h2>

<!-- start:teaserimage -->
<p class='teaserimage'><img src='http://anmutunddemut.de/files/uploads/2007/09/dasmadchenausdemwaser2.jpg' alt='Das Mädchen aus dem Wasser'/></p>
<!-- ende:teaserimage -->
<p>Im Vorfeld hab ich gelesen, dass die Geschichte des Films wie folgt entstanden sei: Herr Shalayman Nightingale - der Name ist schlicht unmerkbar - seinen Kindern von Abend zu Abend eine eine Gutenachtgeschichte erzählt, die er sich immer weiter ausgedacht hat. Das zu wissen hilft, wenn man den Film sieht. Sowohl der Kindergeschichtenkontext als auch das "Während der Zeit den Verlauf der Geschichte weiterdenken"-Ding sind dem Film deutlich anzumerken. Man könnte das als Schwäche auslegen. Weil ich Herrn Nightman aber mag und mir wünsche, dass er tolle Filme macht, lasse ich das mal sein.</p>
<p>Das Mädchen aus dem Wasser, gehört zu einem Volk von Wesen, die in "der blauen Welt" leben und die in ganz alter Zeit die Menschen geholfen haben, sie berieten und ihnen Inspiration waren. Bis die Menschen anfingen, sich von den Meeren und Wassern und deren Bewohnern zu entfernen, und ihnen nicht mehr zuzuhören, bis die Wesen der blauen Welt zu Legenden wurden, Legenden wurden zu Mythen und was nicht in Vergessenheit geraten durfte, wurde vergessen.</p>
<p>Das Mädchen aus dem Wasser ist nun ein Versuch der Blauen Welt, den Menschen noch einmal zu helfen, sie wieder auf den richtigen Weg zu bringen. Sie taucht im Pool einer Apartmentanlage auf. Ihre Aufgabe ist die einer Muse. Sie muss eine Schriftsteller finden und ihn inspirieren und er wird daraufhin ein Buch schreiben, dass die Welt verändern wird.<br />
Ich kriege schon eine Gänsehaut, nur vom Schreiben des letzten Satzes. Nymphen, Musen und Bücher, die die Welt verändern. Huhu, wie wunderbar. Das ist genau - mein - Ding. Und es verleiht der ganzen, fantastischen (im Sinne von Fantasy) Geschichte überhaupt erst die notwendigen Dreh. Dieses Martin Luther King, J.F.K, Ghandi Ding ist es, dass über die Unzulänglichkeiten und merkwürdigen Special Effekts hinwegblicken läßt und den Film schön macht.</p>
        
