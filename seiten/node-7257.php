      <h2>Seven dwarfs, eh things about me you don’t really want to know</h2>

<!-- start:teaserimage -->
<p class='teaserimage'><img src='http://anmutunddemut.de/files/sevendwarves.jpg' alt='Seven dwarfs, eh things about me you don’t really want to know'/></p>
<!-- ende:teaserimage -->
<p><a href="http://powerbook.blogger.de/2009/01/07/444819/seven-dwarfs-eh-things-about-me-you-don%e2%80%99t-really-want-to-know/">Commander in Charge Jog</a> commands, that I ought to write down seven more or less secret facts on myself. </p>
<ol>
<li>Once, I tried to drive a van into a multi-storey car park, unsuccessfully. True story.</li>
<li>I have a irrational fear of losing my teeth.</li>
<li>I distrust my own wishes.</li>
<li>My first Computer was the <a href="http://en.wikipedia.org/wiki/Ericsson_Portable_PC">Ericsson Portable PC</a>, a 8088er with 4.77 MHz processor and 512 kB RAM</li>
<li>I never downloaded a movie.</li>
<li>Actually, I believe that I will never own a car.</li>
<li>I am lame, totally.</li>
</ol>
<p>If one of those five bloggers is not keeping this stupid shit going, it means he does not understand a word English: <a href="http://codecandies.de/">Nico</a>, <a href="http://www.marctv.de/">Marc</a>, <a href="http://arnalyse.blogspot.com/">Arne</a>, <a href="http://konnexus.net/">Konnexus</a>, <a href="http://teilnehmer.ws/">Teilnehmer</a>, <a href="http://blog.flaeche-schub.de/">Eike</a>, <a href="http://www.felixwilleke.com/">Felix</a></p>
<p>Now thou shallst follow the rules:<br />
- Link your original tagger(s), and list these rules on your blog.<br />
- Share seven facts about yourself in the post - some random, some wierd.<br />
- Tag seven people at the end of your post by leaving their names and the links to their blogs.<br />
- Let them know they’ve been tagged by leaving a comment on their blogs and/or Twitter.</p>
        
