      <h2>The day after tomorrow</h2>

<!-- start:teaserimage -->
<p class='teaserimage'><img src='http://anmutunddemut.de/files/uploads/2007/09/thedayaftertomorrow-q.jpg' alt='The day after tomorrow'/></p>
<!-- ende:teaserimage -->
<p>Endlich mal ein Film, der schon im Original einen <a href="http://anmutunddemut.de/2007/02/27/dodgeball/">dämlichen Teilsatztitel</a> hat. Da haben die Übersetzen gleich das Handtuch geworfen. (Päh, jetzt können die Amerikaner sogar dämliche Teilsatztitel machen, dann übersetzen wir den auch nicht, das haben die jetzt davon.) Dabei ist der Film gar nicht von einem Amerikaner. Dafür ist er aber umso amerikanischer. Kann bitte mal jemand Roland Emmerich eine Ehrenausbürgerung überreichen. Das wär doch mal was. Das  Staatsbürgerschafts-Pendant zum  Wegloben. Das wäre witzig. Verdient hätte er es auf jedem Fall. The Day after Tomorrow ist, wie Independence Day auch schon sauber durchdekliniert. Ich möchte fast behaupten, Emmerich hat es zu einer perfiden Form der Perfektion gebracht. Die bedeutungsschwangeren Szenen - Wissenschaftler in irgend einer Station, blinkende Monitore - Schnitte nach Indien - und dann die großen Szenen, große Wolken, große Raumschiffe, große Wellen, sechs Tornados auf einmal die Downtown L.A. zertrümmern.</p>
<p><img id="image4418" src="http://anmutunddemut.de/files/uploads/2007/03/losangelesimwind.jpg" alt="Los Angeles im Wind" /></p>
<p>Apropos "groß" und "zertrümmern":  schaut man sich Emmerichs große Filme an - ID4, Godzilla und jetzt TDaT - da ist auffällig, dass er eines am liebsten macht - Amerika kapottmachn, richtig in Schutt und Schnee legen. Das ist ja doch schon ein bischen suspekt. Vieleicht ist das irgendeine subtile Form von deutscher Rache. Oder eine subtile Form von Amerikanischem Unterbewußtstein. Immerhin lieben die Amis ihn dafür. Vielleich wissen sie irgendwo in ihrem Unterbewußten um all das Elend, das sie gebracht haben uns sehen sich nach Betrafung, nach einem Gericht, oder besser noch nach Demut.</p>
<p>"Die zerstörerische Gewalt, die wir in den letzten Wochen erlebt haben, hat uns alle mit tiefer Demut erfüllt", sagt der Ex-Vize-Nun-Präsident am Ende. Hört! Hört! Demut! Würde ja zum protestantischen Weltbild passen. Ich bin trotzdem aber erstaunt, wie amerikanisch auch das schon wieder ist. Demut wird erzeugt durch Gewalt. Meine Scheiße, dass ist noch sunerträglicher, als die ganzen Hahnebücheneien des Films. Hat Emmerich mal wer erzählt, das es z.B. in einem handelüblichen Iglu die Temperatur immer etwa um den Gefrierpunkt ist, nie tiefer? Egal, Fakten sind nicht sein Ding, so ist das mit Germanoamerikanern.</p>
<p><img id="image4419" src="http://anmutunddemut.de/files/uploads/2007/03/misslibertyimschnee.jpg" alt="Miss Liberty im Schnee" /></p>
        
