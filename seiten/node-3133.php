      <h2>Spaced</h2>

<!-- start:teaserimage -->
<p class='teaserimage'><img src='http://anmutunddemut.de/files/uploads/2007/09/spaced.jpg' alt='Spaced'/></p>
<!-- ende:teaserimage -->
<p>Bis in die spätern 20er meines Lebens fand ich es eigentlich ziemlich cool Dinge zu finden, zu kennen und zu verbreiten, die sonst nicht so viele kannten; er erste zu sein er ein total bizarres Undergroundcomic hat, bevor der Hype losgeht; den Freundeskreis mit einem schrägen asiatischen Film begeistern. Das hat Spass gemacht. In den letzten Jahren hab ich die Freude daran etwas verloren, was ich mla darauf zurückführe, dass es ja auch einer gewissen Mühe bedarf solche Dinge auszubuddeln, und in der Regel kommt auf einen Kracher wenigsten ein Griff ins Klo.</p>
<p><img alt="Spaced Wallpaper" id="image2852" src="http://anmutunddemut.de/files/uploads/2006/01/spaced.jpg" /></p>
<p>(v.l.n.r: Daisy, Twist, Marsha, Tim, Brian, Mike)</p>
<p>Bei Spaced muss ich eine Ausnahme machen. Gut, der Ruhm für die Entdeckung gilt <a href="http://www.endeneu.de/archives/2004/10/03/einfach-mal-gekauft/">Endeneu</a>, der mir beim letzten Besuch die DVD der ersten Season dieser britischen Serie mit dem Kommentar "Das <em>musst</em> Du mitnehmen" in die Hand und aufs Auge gedrückt hat. Die Grundgeschichte ist schnell erzählt. Tim und Daisy geben vor ein Paar zu sein, um eine Wohnung zu bekommen. Tim versucht Comiczeichner zu sein und Daisy versucht Journalistin zu werden. Ihre Freunde Twist und Mike haben auch ordentlich einen ander Klatsche, wirken aber noch wie normale Menschen, gegenüber Marsha der Vermieterin und Brian dem Künstler der - zurecht und sehr bezeichnend - im Keller wohnt.</p>
<p>Der durchweg britische Humor läßt sich am schönsten an meiner Lieblingszene erklären. Tim und Daisy, die gerade eingezogen sind, haben plötzlich Brian in ihrer Küche sitzen, den Tim im Keller getroffen hat. Auf die Frage, was Brian den so für Kunst mache antwortet er "Anger - Pain - Fear - Aggression." illustirert von lustigen Dingen die Brian in seinem Keller treibt und die allesamt ins Verhaltensmuster "total krank" gehören. Daraufhin fragt Daisy: "Watercolors or...?" was Brian mit einem lakonischen "It's a bit more complex than that." beantwortet. Dadurch ein bischen verunsichert, was bei Daisy fast ständig der Fall ist, mein sie noch so "Tim does cartoons.", was sofort und wehement von Tim mit " It's a bit more complex than that." kommentiert.<br />
Ich könnt mich wegschmeißen.</p>
<p>Wie sooft in letzter Zeit muss ich festhalten, dass Spaced deshalb so genial ist, weil die Figuren mindestens 5000 mal authenitischer wirken als die aus amerikanischen Comedy-Serien. Friends wirken gegen normalen Irren aus Spaced wie Kens und Barbies im Spassland. Es tut gut, zu sehen, wie Tim - noch auf billigem Speed - halb besessen, halb bewußtlos Playstation spielt und Zombies umwimmst. Herrlich. Herzerfrischend. Ka und ich haben gestern abend die ganze erste Staffel druchgezogen. Sieben Folgen. Einzig der Mangel an Nachschub verhinderte schlimmeres. Leute, Ihr müsst alle Spaced sehen!</p>
        
